    private void editPersonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editPersonActionPerformed
        // TODO add your handling code here:       
        Person obj = new Person();
        obj.setNombre(this.txtName.getText());
        obj.setApellido(this.txtLastName.getText());
        obj.setMatricula(this.txtMat.getText());
        obj.setDireccion(this.txtDirec.getText());
        obj.setEdad(Integer.parseInt(this.txtAge.getText()));
        obj.setCarerra(this.txtCarrera.getText());
        this.operations.updateStudent(this.tableRef, obj);
        this.hide();
    }//GEN-LAST:event_editPersonActionPerformed

    private void removePersonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removePersonActionPerformed
        this.operations.deleteStudent(this.tableRef, new Person(this.person.getMatricula()));
        this.hide();
    }//GEN-LAST:event_removePersonActionPerformed

    private void setPersonText() {
        this.txtName.setText(this.person.getNombre());
        this.txtCarrera.setText(this.person.getCarerra());
        this.txtDirec.setText(this.person.getDireccion());
        this.txtLastName.setText(this.person.getApellido());
        this.txtMat.setText(this.person.getMatricula());
        this.txtAge.setText(String.valueOf(this.person.getEdad()));
    }

