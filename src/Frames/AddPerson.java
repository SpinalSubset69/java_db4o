    private void addPersonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addPersonMouseClicked
        try {
            Person obj = new Person();
            obj.setNombre(this.txtName.getText());
            obj.setApellido(this.txtLastName.getText());
            obj.setDireccion(this.txtDirec.getText());
            obj.setCarerra(this.txtCarrera.getText());
            obj.setMatricula(this.txtMat.getText());
            obj.setEdad(Integer.parseInt(this.txtAge.getText()));
            this.operations.addStudent(this.tableRef, obj);
            this.hide();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }

    }//GEN-LAST:event_addPersonMouseClicked


